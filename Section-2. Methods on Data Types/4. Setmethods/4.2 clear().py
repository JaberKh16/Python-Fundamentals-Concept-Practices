'''
    # Syntax : set.clear()
    # clear() doesn't take any parameter
    # Removes all element from the set, e.g set()
    # It doesnt return any value e.g None

'''

#works with clear()
v = {"a", "e", "i", "e", "u", 24}
print(v)
#returns None
print(v.clear())
#printing the set
#returns empty set e.g set()
print(v)
