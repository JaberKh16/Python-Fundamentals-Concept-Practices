"""
    String Method: upper()
    ======================
    # Syntax : str.upper()
    # upper() doesn't take any parameters.
    # Return the string by converting the string in uppercase characters.
    # If uppercase string is given it doesnt modify the string though already having uppercase characters.
"""

# when string is in uppercase
p = "I AM AWSOME"
print(p.upper())

# when string is having both upper and lower case

p = "I am AwsoME"
print(p.upper())

# when string is in lowercase
p = "i know im bad at programming till now"
print(p.upper())

# alphanumeric string
p = "22PyThON"
print(p.upper())
