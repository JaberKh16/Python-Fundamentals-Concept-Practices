"""
    String Method: lower()
    ======================
    # Syntax : str.lower()
    # lower() doesn't take any parameters.
    # Returns the string by converting the string in lowercase.
    # If lower string is given it doesnt modify the string though already having lowercase.
"""

# when string is in uppercase
p = "I AM AWSOME"
print(p.lower())

# when string is having both upper and lower case
p = "I am AwsoME"
print(p.lower())

# when string is in lowercase
p = "i know im bad at programming till now"
print(p.lower())

# alphanumeric string
p = "22PYTHON"
print(p.lower())
