"""
    String Method: capitalize()
    ===========================
    # Syntax : str.capitalize()
    # capitalize() doesn't take any parameter.
    # Returns The First Character as a Uppercase Character.
"""

# With  Alphabets
x = "i am awsome"
print(x)

# Capitalizing The First Word
y = x.capitalize()
print(y)

# With Non Alphabetical First Character
x = "+ am awsome"
print(x)
# Capitalizing The First Word
y = x.capitalize()
print(y)