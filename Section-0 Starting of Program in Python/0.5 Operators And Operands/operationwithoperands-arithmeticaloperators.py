'''
    Arithmetic Operators in Python
    ===============================
    # Operators in Python (+, -, *, /, //, %) same as the other language have.
'''

# int + with int ----> int type
print(5+4)

# int + with float ---> float type
print(5+ 5.34)

# float + float ---> float type
print(3.23 + 5.42)


# Python leaves the 0's after decimal point values on float or double
print(32.234000000) # here Python Leaves the extra zero(0) after float or double

# Multiplication
print(10*23)

# division returns the float
print(10/3)

# division returns the int value
print(10//3)

# modulus returns the remainder
print(10%10)